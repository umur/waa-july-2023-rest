package online.moumini.lab2.model;

public class Course {
    private int id;
    private String name;
    private String code;

    public Course() {
    }
    
    public int getId() {
        return id;
    }
    public String getName() {
        return name;
    }
    public String getCode() {
        return code;
    }
    public void setId(int id) {
        this.id = id;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setCode(String code) {
        this.code = code;
    }

    

}
